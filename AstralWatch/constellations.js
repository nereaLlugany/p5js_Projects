function drawStaticStars() {
  fill(255);
  noStroke();
  for (let star of stars) {
    ellipse(star.x, star.y, 2, 2);
  }
}

function drawConstellations(h) {
  let hourIndex = h % 12; // Obtenir l'índex de l'hora actual (modul 12 per a fer cíclic)
  let constellationName;
  let i = 0;
  for (let name in constellations) {
    if (i === hourIndex) {
      constellationName = name;
      break;
    }
    i++;
  }

  let constellationPoints = constellations[constellationName]; // Obté els punts de la constel·lació
  for (let point of constellationPoints) {
    stroke(color(255, 130, 150, 150));
    fill(color(255, 130, 150, 150));
    ellipse(point.x * 2 + 1, point.y * 2 + 1, 8, 8);
    stroke(255, 130, 150);
    fill(255, 130, 150);
    ellipse(point.x * 2, point.y * 2, 5, 5); // Escala els punts per ajustar-se al canvas
  }
  
   // Obtenir les línies de la constel·lació actual
  let constellationLines = constellationsLines[constellationName];
    for (let lineConst of constellationLines) {
      stroke(color(255, 130, 150, 150));
      strokeWeight(2);
      line(lineConst.x1 * 2, lineConst.y1 * 2, lineConst.x2 * 2, lineConst.y2 * 2); // Escalant els punts per ajustar-se al canvas
    }
 
}

